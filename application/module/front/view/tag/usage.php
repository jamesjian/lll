1. 关键词用于用户搜索和问题分类， 比如可以用 ”教育“ 或 ”fitness“ 作为关键词， 每个问题或广告可以最多有5个关键词。<br /><br />
2. 请使用中英文词汇作为关键词， 非中英文词汇的符号会被自动清除， 比如!  @  $ * = < > 《 》等等。 
多余的空格请不要保留， 比如”悉尼大学“不要输成”悉尼 大学“， 英文关键词可以有空格， 比如”sydney university"。 <br /><br />
3. ”悉尼大学“是一个关键词，”悉尼“和”大学“则是两个关键词， 如果您想搜索”悉尼大学“， 请将”悉尼大学“放在在一个关键词的输入框内， 而不要在两个输入框内分别输入”悉尼“和”大学“<br /><br />
4. 大的区域名称一般不作为关键词， 如“纽省”， “维多利亚州”， “ACT", "TASMANIA"等， 请在区域选项中选择。<br /><br />
